"""В исходную строку добавили некоторое количество символов.
Необходимо выявить, была ли строка a исходной для строки b.
4 cпособ: 2 указателя
"""


def isSubsequence(a, b):
    l1, l2 = len(a), len(b)
    p1 = p2 = 0
    while p1 < l1 and p2 < l2:
        if a[p1] == b[p2]:
            p1 += 1
            p2 += 1
        else:
            p2 += 1
    return p1 == l1
